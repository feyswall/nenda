    {
        $commentData = $this->validate($request, array(
            'name'      =>  'required|max:655',
        ));


        Auth::guard('assistance-lecture')->user()->assistanceComment()->create($commentData);

        Session::flash('done', 'Comment was added');
        return redirect()->route('assistance-lecture.start');
    }

